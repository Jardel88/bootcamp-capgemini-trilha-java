package operadores;

public class OperadoresAritmeticos {
    public static void main(String[] args) {
        int num1, num2, resultado;
        //Soma
        num1 = 10;
        num2 = 2;
        resultado = num1 + num2;
        System.out.println("Resultado é: " + resultado);

        //Subtração
        num1 = 10;
        num2 = 2;
        resultado = num1 - num2;
        System.out.println("Resultado é: " + resultado);

        //Multiplicação
        num1 = 10;
        num2 = 2;
        resultado = num1 * num2;
        System.out.println("Resultado é: " + resultado);

        //Divisão
        num1 = 10;
        num2 = 2;
        resultado = num1 / num2;
        System.out.println("Resultado é: " + resultado);
    }
}
